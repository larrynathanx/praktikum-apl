/*TIPE DATA PRIMITIF
Tipe data yang digunakan untuk operasi dasar
--------------------------------------------
int = 9 => 2 byte
float = 9.9 => 4 byte
double = 9.9 => 9 byte
char = 'a' => 1 byte
bool = true/false => 1 byte
string = "Hello, World!"
*/

/*TIPE DATA KOLEKTIK
Kumpulan data yang memiliki indeks
---------------------------------
int angka[5] = {1, 2, 3, 4, 5};
*/

/*TIPE DATA ABSTRAK
Struct adalah sekumpulan variabel bertipe
data abstrak yang dinyatakan dengan sebuah nama dan bisa diciptakan secara dinamis.
----------------------------------------------------------------------------------
struct Mahasiswa {
string nama;
int umur;
float ipk;
};
Mahasiswa mhs = {"Sipuwah", 20, 3.5};
*/

/*VARIABEL DAN ATURAN MENULISNYA
--------------------------------
- Harus diawali huruf atau underscore
- Tidak boleh dimulai dengan angka
- Tidak boleh ada spasi
- Tidak boleh karakter spesial selain underscore
- Case Sensitive
- Tidak boleh pakai keyword C++
- Boleh pakai underscore di awal atau akhir
*/

/*OPERATOR
----------
Penjumlahan (+)
Pengurangan (-)
Perkalian (*)
Pembagian (/)
Sisa Bagi (%)
Increment (++)
Decrement (--)
*/

/*OPERATOR PENUGASAN
--------------------
Penugasan (=)
Penambahan (+=)
Pengurangan (-=)
Perkalian (*=)
Pembagian (/=)
Sisa bagi (%=)
Pergeseran bit ke kiri (>>=)
Pergeseran bit ke kanan (<<=)
Bitwise AND (&=)
Bitwise OR Eksklusif (^=)
Bitwise OR Inklusif (!=)
*/

/*OPERATOR PERBANDINGAN
Operator untuk membandingkan dua operand
Sama dengan (==)
Tidak sama dengan (!=)
Kurang dari (<)
Lebih dari (>)
Kurang dari sama dengan (<=)
Lebih dari sama dengan (>=)
*/

/*OPERATOR LOGIKA
Operator logika digunakan untuk menggabungkan dua atau lebih ekspresi logika. Operator
ini menghasilkan nilai true atau false.
--------------------------------------------------------------------------------------
AND (&&)
OR (||)
NOT (!)
*/

/*OPERASI BITWISE
Operator bitwise adalah operasi matematika yang mengoperasikan pada bilangan biner
berbasis 2.
----------------------------------------------------------------------------------
AND (&)
OR (|)
XOR (^)
NOT (~)
Pergeseran bit ke kiri (<<)
Pergeseran bit ke kanan (>>)
*/

/*OPERASI TERNARY
Operator ternary adalah operator yang memungkinkan kita untuk menulis kode if-else
dalam satu baris. Operator ini terdiri dari tiga bagian, yaitu kondisi, ekspresi jika benar, dan
ekspresi jika salah.
------------------------------------------------------------------------------------------------
Penulisan 
kondisi ? ekspresi_jika_benar : ekspresi_jika_salah
*/

